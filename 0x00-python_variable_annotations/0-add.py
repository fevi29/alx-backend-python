#!/usr/bin/env python3
"""
Defines a function called 'add'
"""


def add(a: float, b: float) -> float:
    """
    This function adds two numbers and returns the result.

    Parameters:
    a (float): The first number.
    b (float): The second number.

    Returns:
    float: The sum of a and b.
    """
    return a + b
